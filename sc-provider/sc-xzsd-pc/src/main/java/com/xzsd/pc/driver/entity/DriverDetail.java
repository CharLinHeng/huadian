package com.xzsd.pc.driver.entity;

public class DriverDetail {
    /**
     * 司机名称
     */
    private String driverName;
    /**
     * 司机账号
     */
    private String driverAcct;
    /**
     * 司机手机
     */
    private String driverPhone;


    /**
     * 司机密码
     */
    private String driverPass;

    /**
     * 司机身份证
     */
    private String driverIdCard;

    /**
     * 司机身份名称
     */
    private String driverProvinceName;

    /**
     * 司机城市名称
     */
    private String driverCityName;

    /**
     * 司机市区名称
     */
    private String driverDistinctName;

    public String getDriverName() {
        return driverName;
    }

    public void setDriverName(String driverName) {
        this.driverName = driverName;
    }

    public String getDriverAcct() {
        return driverAcct;
    }

    public void setDriverAcct(String driverAcct) {
        this.driverAcct = driverAcct;
    }

    public String getDriverPhone() {
        return driverPhone;
    }

    public void setDriverPhone(String driverPhone) {
        this.driverPhone = driverPhone;
    }


    public String getDriverPass() {
        return driverPass;
    }

    public void setDriverPass(String driverPass) {
        this.driverPass = driverPass;
    }

    public String getDriverIdCard() {
        return driverIdCard;
    }

    public void setDriverIdCard(String driverIdCard) {
        this.driverIdCard = driverIdCard;
    }

    public String getDriverProvinceName() {
        return driverProvinceName;
    }

    public void setDriverProvinceName(String driverProvinceName) {
        this.driverProvinceName = driverProvinceName;
    }

    public String getDriverCityName() {
        return driverCityName;
    }

    public void setDriverCityName(String driverCityName) {
        this.driverCityName = driverCityName;
    }

    public String getDriverDistinctName() {
        return driverDistinctName;
    }

    public void setDriverDistinctName(String driverDistinctName) {
        this.driverDistinctName = driverDistinctName;
    }
}


